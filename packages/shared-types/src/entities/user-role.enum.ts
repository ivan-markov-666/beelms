/**
 * Enum для ролей пользователей в системе
 * @enum {string}
 */
export enum UserRole {
  /**
   * Администратор системы с полным доступом
   */
  ADMIN = 'admin',

  /**
   * Инструктор, который может создавать и редактировать контент
   */
  INSTRUCTOR = 'instructor',

  /**
   * Обычный пользователь с ограниченными правами
   */
  STUDENT = 'student',
}
